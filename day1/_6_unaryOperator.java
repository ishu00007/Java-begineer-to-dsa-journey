package day1;

public class _6_unaryOperator {
    public static void main(String[] args) {
        int num = 15;

        System.out.println(num++);// it first uses num value and then add 1 to
        System.out.println(++num);// it first add 1 to num value and thes uses it
        System.out.println(num--);
        System.out.println(--num);
    }
}
